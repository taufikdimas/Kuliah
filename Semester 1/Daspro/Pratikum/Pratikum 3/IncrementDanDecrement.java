public class IncrementDanDecrement {
    public static void main(String[] args) {
        int i = 5;
    //increment
        System.out.println("i :" + i); 
        System.out.println("++i : " + ++i);
        System.out.println("i++ : " + i++);
    //Decrement
        System.out.println("--i : " + --i);
        System.out.println("i--: " + i--);
        System.out.println("1 : " + 1);
    }
}